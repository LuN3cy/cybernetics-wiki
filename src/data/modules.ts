function ref(section: string, pages: string, excerpt: string, note: string) {
  const pageNumber = Number(pages.match(/\d+/)?.[0] ?? 1);
  return {
    chapter: section.startsWith("1.")
      ? "第一章 控制和反馈"
      : section.startsWith("2.")
        ? "第二章 信息、思维和组织"
        : section.startsWith("3.")
          ? "第三章 系统及其演化"
          : section.startsWith("4.")
            ? "第四章 质变的数学模型"
            : "第五章 黑箱认识论",
    section,
    pages,
    pageNumber,
    pageImage: `pdf-pages/page-${pageNumber}.png`,
    highlight: highlightMap[pageNumber] ?? { x: 9, y: 10, width: 82, height: 64 },
    excerpt,
    note,
  };
}

function source(section: string, pages: string, title: string, body: string, excerpt: string, note: string): CaseStudy {
  return {
    source: "书中案例",
    title,
    body,
    reference: ref(section, pages, excerpt, note),
  };
}

function modern(title: string, body: string): CaseStudy {
  return { source: "现代工程映射", title, body };
}

function q(prompt: string, options: string[], answer: number, explanation: string, concept: string): QuizQuestion {
  return { prompt, options, answer, explanation, concept };
}

function moduleItem(input: Omit<ChapterModule, "quiz"> & { quiz?: QuizQuestion[] }): ChapterModule {
  return {
    ...input,
    quiz: input.quiz ?? [
      q(
        `${input.index} 最核心的判断是什么？`,
        ["看见可能性如何被缩小", "背下案例名称", "忽略实验反馈", "只追求一次命中"],
        0,
        "这一应用的阅读目标不是记忆名词，而是在实验里看见可能性空间、信息或反馈结构如何变化。",
        input.title,
      ),
    ],
  };
}

